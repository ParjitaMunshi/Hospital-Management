    private void DoctorUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DoctorUpdateBtnActionPerformed
        // TODO add your handling code here:
         for (Doctor D : history.getHistory()){
         
            if(DoctorIdText.getText().equals(String.valueOf(D.getDoctorID()))) {
                
                D.setDoctorName(DoctorNameText.getText());
                D.setHospitalID(Integer.parseInt(HospitalIDText.getText()));  
                D.setDoctorEmailAddress(DoctorEmailIDText.getText());
                D.setDoctorContactNumber(Double.parseDouble(DoctorContactNumberText.getText()));
            }
               JOptionPane.showMessageDialog(this, "Community Details Updated");
               
        }
      

    }//GEN-LAST:event_DoctorUpdateBtnActionPerformed

