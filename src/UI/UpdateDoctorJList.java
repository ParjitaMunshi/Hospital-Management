    private void DoctorSerachBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DoctorSerachBtnActionPerformed
        // TODO add your handling code here:
        int DoctorID = Integer.parseInt(DoctorIdText.getText());
       
       for (Doctor D : history.getHistory()) {
        if (DoctorID == D.getDoctorID()) {
                DoctorNameText.setText(D.getDoctorName());
                HospitalIDText.setText(String.valueOf(D.getHospitalID()));
                DoctorEmailIDText.setText(D.getDoctorEmailAddress());
                DoctorContactNumberText.setText(String.valueOf(D.getDoctorContactNumber()));                
                
                }
       }
                                                     
        // TODO add your handling code here:
     
    }//GEN-LAST:event_DoctorSerachBtnActionPerformed

    private void DoctorIdTextKeyReleased(java.awt.event.KeyEvent evt) {                                         
        // TODO add your handling code here:
        String PATTERN = "^[0-9]{0,4}$";
        Pattern patt = Pattern.compile(PATTERN);
        Matcher match = patt.matcher(DoctorIdText.getText());
        
        if(!match.matches()){            
            JOptionPane.showMessageDialog(this,"Invalid ! Please Enter Valid ID ");
        }
    } 
    private void DoctorEmailIDTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_DoctorEmailIDTextKeyReleased
        // TODO add your handling code here:
       String PATTERN = "^[a-zA-Z0-9(.+)@(\\\\S+)]{0,40}$";
       Pattern patt = Pattern.compile(PATTERN);
       Matcher match = patt.matcher(DoctorEmailIDText.getText());

       if(!match.matches()){            
           JOptionPane.showMessageDialog(this,"Invalid ! Please Enter Valid EmailAdress ");
       }
    }//GEN-LAST:event_DoctorEmailIDTextKeyReleased
