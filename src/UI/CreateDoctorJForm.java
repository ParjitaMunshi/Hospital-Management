    private void DoctorSaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DoctorSaveBtnActionPerformed
        // TODO add your handling code here:
        int DoctorID  =  Integer.parseInt(DoctorIdText.getText());
        String DoctorName = DoctorNameText.getText();
        int HospitalID  =  Integer.parseInt(HospitalIDText.getText());
        Double DoctorContactNumber = Double.parseDouble(DoctorContactNumberText.getText());
        String DoctorEmailId = DoctorEmailIDText.getText();

        Doctor D = history.addNewDoctor();
        D.setDoctorID(DoctorID);
        D.setDoctorName(DoctorName);
        D.setHospitalID(HospitalID);
        D.setDoctorContactNumber(DoctorContactNumber);
        D.setDoctorEmailAddress(DoctorEmailId);

        JOptionPane.showMessageDialog(this,"New Doctor Detail Added.");

        DoctorIdText.setText("");
        DoctorNameText.setText("");
        HospitalIDText.setText("");
        DoctorContactNumberText.setText("");
        DoctorEmailIDText.setText("");
        
    }//GEN-LAST:event_DoctorSaveBtnActionPerformed

