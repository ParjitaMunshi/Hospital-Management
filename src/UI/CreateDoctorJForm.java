    private void DoctorIdTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_DoctorIdTextKeyReleased
        // TODO add your handling code here:
        String PATTERN = "^[0-9]{0,4}$";
        Pattern patt = Pattern.compile(PATTERN);
        Matcher match = patt.matcher(DoctorIdText.getText());
        
        if(!match.matches()){            
            JOptionPane.showMessageDialog(this,"Invalid ! Please Enter Valid ID ");
        }
    }//GEN-LAST:event_DoctorIdTextKeyReleased

