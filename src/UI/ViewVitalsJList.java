    private void VitalsDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VitalsDeleteBtnActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex = VitalsTable.getSelectedRow();
       
       if (selectedRowIndex<0){           
           JOptionPane.showMessageDialog(this, "Please select a row to delete.");
           return;
       }
       
       DefaultTableModel model = (DefaultTableModel) VitalsTable.getModel();
       Vitals selectedCommunity = (Vitals) model.getValueAt(selectedRowIndex,0);
       history.deleteVitals(selectedCommunity);
       JOptionPane.showMessageDialog(this, "Community signs delete.");
       
       populateTable();
    }//GEN-LAST:event_VitalsDeleteBtnActionPerformed


