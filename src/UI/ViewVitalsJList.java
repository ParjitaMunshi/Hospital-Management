    private void populateTable() {
           DefaultTableModel model = (DefaultTableModel) VitalsTable.getModel();
            model.setRowCount(0);

            for (Vitals V : history.getHistory()){

                   Object[] row = new Object[3];
                   row[0] = V;
                   row[1] = V.getHeartRate();
                   row[2] = V.getBodyTemperature();
                   
                   model.addRow(row);
                 
            } 
    }

